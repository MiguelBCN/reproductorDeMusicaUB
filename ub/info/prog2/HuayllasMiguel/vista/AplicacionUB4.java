    private void onAddFileDialogBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onAddFileDialogBtnActionPerformed
        //Conseguir la info de los otros componentes 
        String ruta = fileTxtFile.getText();
        String caratula = caratulaTxtFile.getText();
        String autor = autorTxtFile.getText();
        String codec;

        if (caratula.equals("")) {
            caratula = "C:\\Users\\MiguelDesktop\\Documents\\reproductorNetbeans\\iconMusic.jpg";
        }
        //Mandamos que se ejecute el controlador
        if (tipoFileComboBox.getItemAt(tipoFileComboBox.getSelectedIndex()).equals("Musica")) {
            codec = "mp3";
            try {
                control.addAudio(ruta, caratula, autor, codec, 1);
            } catch (ReproException e) {
                System.out.println(e.getMessage());
            }
        } else {
            codec = "jpg";
            try {
                control.addImatge(ruta, autor, codec, 1, 1);
            } catch (ReproException e) {
                System.out.println(e.getMessage());
            }
        }

        addFileDialog.dispose();


    }//GEN-LAST:event_onAddFileDialogBtnActionPerformed

